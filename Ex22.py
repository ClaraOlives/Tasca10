def crear_punts():
    l1=[2,3,1]
    l2=[1,2,5]
    print(l1)
    for e in l1:
        p='.'
        p*=e
        print("Els nombres representats en punts: {}".format(p))
    print(l2)
    for e in l2:
        p='.'
        p*=e
        print("Els nombres representats en punts: {}".format(p))





crear_punts()